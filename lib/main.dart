import 'package:flutter/material.dart';
import 'package:flutter_riverpod/flutter_riverpod.dart';
import 'package:firebase_core/firebase_core.dart';
import 'package:firebase_auth/firebase_auth.dart';
import 'package:cloud_firestore/cloud_firestore.dart';
import 'package:firebase_crashlytics/firebase_crashlytics.dart';
import 'package:hive_flutter/hive_flutter.dart';
import 'firebase_options.dart'; // Generated by flutterfire configure
import 'core/config/app_config.dart';
import 'core/services/offline_service.dart';
import 'app.dart';

void main() async {
  WidgetsFlutterBinding.ensureInitialized();

  // Initialize Hive for local storage
  await Hive.initFlutter();

  try {
    // Initialize Firebase
    await Firebase.initializeApp(
      options: DefaultFirebaseOptions.currentPlatform,
    );

    // Optional: Enable Firestore offline persistence
    FirebaseFirestore.instance.settings = const Settings(
      persistenceEnabled: true,
      cacheSizeBytes: Settings.CACHE_SIZE_UNLIMITED,
    );

    // Optional: Set up Firebase Crashlytics
    FlutterError.onError = FirebaseCrashlytics.instance.recordFlutterFatalError;

    // Register Hive adapters (simplified without code generation)
    await OfflineService.registerAdapters();

    // Initialize offline storage
    await OfflineService.initialize();

    // Optional: Check if user is already logged in
    final User? currentUser = FirebaseAuth.instance.currentUser;
    if (currentUser != null) {
      debugPrint('User already logged in: ${currentUser.email}');
    }

    runApp(
      const ProviderScope(
        child: TurboAirApp(),
      ),
    );
  } catch (e) {
    debugPrint('‚ùå Firebase initialization error: $e');

    // Show error UI if Firebase fails to initialize
    runApp(ConfigErrorApp(error: e.toString()));
  }
}

